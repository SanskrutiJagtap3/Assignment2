# comparision operators
d=10
e=5
print("comparision operators!!")
print("10>5:", d>e)
print("10<5:",d<e)
print("10==5:",d==e)
print("10!=5",d!=e)

#logical operators

a=True
b=False
print("logical operators!!")
print("a and b:",a and b)
